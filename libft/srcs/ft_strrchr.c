#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;

	p = (char *)s + ft_strlen(s);
	while (*p != (char)c && p > s)
		p--;
	if ((char)c == *p)
		return (p);
	return (NULL);
}
